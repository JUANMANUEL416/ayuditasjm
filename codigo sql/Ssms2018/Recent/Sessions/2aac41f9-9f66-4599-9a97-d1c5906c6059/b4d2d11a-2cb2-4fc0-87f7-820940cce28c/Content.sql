SELECT NIEPE.CNSPAGO,NIEPE.ANO,NIEPE.MES, NEMP.DOCIDEMPLEADO,NIEPE.NOMBRE_EMPLEADO,NCARNG.DESCRIPCION [CARGO],
DBO.FNK_VALOR_CONCEPTO_NOMI_NIEPED(NIEPE.CNSNIEPE,NIEPE.NUMDOC,'CN40') [PROVISION CESANTIAS],
DBO.FNK_VALOR_CONCEPTO_NOMI_NIEPED(NIEPE.CNSNIEPE,NIEPE.NUMDOC,'CN42') [PROVISION INT CESANTIAS],
