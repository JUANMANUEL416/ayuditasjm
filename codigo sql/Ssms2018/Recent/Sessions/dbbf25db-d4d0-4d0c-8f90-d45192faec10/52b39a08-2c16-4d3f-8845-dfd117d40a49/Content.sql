FROM [dbo].[FTR] INNER JOIN  TER ON FTR.IDTERCERO=TER.IDTERCERO  LEFT JOIN AFI ON FTR.IDAFILIADO=AFI.IDAFILIADO 
LEFT JOIN PLN ON FTR.IDPLAN=PLN.IDPLAN WHERE (FTR.N_FACTURA LIKE '%%%%' OR  TER.RAZONSOCIAL LIKE '%%%%') 
AND 1=1 AND CONVERT(DATE, FTR.F_FACTURA) >=  '2024-09-02' AND CONVERT(DATE, FTR.F_FACTURA) <= '2024-12-27' 
AND COALESCE(FTR.FACTEP,'')='101'  AND COALESCE(FTR.IDSEDE,'')='01'  
ORDER BY  FTR.FECHAFAC DESC, FTR.N_FACTURA DESC  OFFSET (1-1)*10 ROWS  FETCH NEXT 10 ROWS ONLY 