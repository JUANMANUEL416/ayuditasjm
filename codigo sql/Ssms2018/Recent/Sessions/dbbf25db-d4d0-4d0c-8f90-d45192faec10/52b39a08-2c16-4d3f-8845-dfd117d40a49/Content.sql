FTR.NOREFERENCIA,FTR.IDPLAN,FTR.VALORSERVICIOS,CASE COALESCE(COPAPROPIO,0) WHEN 1 THEN CP_VLR_COPAGOS ELSE VALORCOPAGO END VALORCOPAGO,
VALORMODERADORA,FTR.VR_TOTAL,FTR.ESTADO,COALESCE(FTR.TIPOANULACION,'') NC,
CASE COALESCE(FACTE,0) WHEN 0 THEN 'Sin Notificar' WHEN 1 THEN 'En Cola' WHEN 2 THEN 'Notificada' WHEN 3  
THEN 'Con Errores' WHEN 4 THEN  'Procesando' END ESTDIAN,FTR.CONTABILIZADA,FTR.CNSFCT AS CNSFTR,FTR.VR_ABONOS AS ABONOS ,
FTR.CAPITADA,CP_CONVENIO AS CONVENIO,COPAGOP = COALESCE(COPAPROPIO,0),VLRCOPAPRO = COALESCE(CP_VLR_COPAGOS,0),DESCUENTO,
F_INICIAL = FECHACAP_INI,F_FINAL = FECHACAP_FIN,FTR.IDSEDE,CP_MES AS MES,MODALIDAD = CP_MODALIDAD,FTR.OBSERVACION,
TIPOFINAN = TIPOFIN,PLN.DESCPLAN,USUARIOFACTURA,CP_VLR_SERVICIOS,CP_VLR_COPAGOS,AFI.DOCIDAFILIADO,AFI.NOMBREAFI,AFI.EDAD,
COALESCE(FTR.NROCOMPROBANTE,'')NROCOMPROBANTE,CASE WHEN EXISTS(SELECT 1 FROM FTRAUD WHERE FTRAUD.N_FACTURA = FTR.N_FACTURA) THEN 'Con Nota' 
ELSE 'Sin Nota' END AS ESTNOTAUD,FACTEP =
CASE COALESCE(FACTEP,'') WHEN '' THEN 'Sin Notificar' WHEN '103' THEN 'Error Valida' WHEN '104' THEN 'Rechazado' WHEN '104A' THEN 'Rechazado-api' WHEN '105' THEN 'Anulada' WHEN '108' THEN 'Baja' ELSE 'OK' END,
