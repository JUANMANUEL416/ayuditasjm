 USE KPCEM
 
 SELECT * FROM 

 SELECT NUMDOC,CODCARGO,NIVEL,GRADO,FECHAINGRESO,FECHARETIRO 
      FROM NEMP
      WHERE ESTADO='Activo'
      AND COALESCE(FECHARETIRO,GETDATE()+1)>GETDATE()

      SELECT * FROM NEMP WHERE DOCIDEMPLEADO='43208517'

      SELECT  NEMP.DOCIDEMPLEADO,NEMP.PRIMERNOMBRE+' '+COALESCE(NEMP.SEGUNDONOMBRE,'')+' '+NEMP.PRIMERNOMBRE+' '+COALESCE(NEMP.SEGUNDONOMBRE,''),
      NEMPV.NDIAS,NEMPV.DIASG,NEMPV.DIASP,NEMPV.DIASG-NEMPV.DIASP DIASP
      FROM NEMPV INNER JOIN NEMP ON  NEMPV.NUMDOC=NEMP.NUMDOC
                 LEFT JOIN NEMPVD ON NEMPV.NUMDOC=NEMPVD.NUMDOC AND NEMPV.ITEM=NEMPVD.ITEM
      WHERE NEMPV.NUMDOC='0100000637' 
      AND NEMPV.ITEM=5

      SELECT * FROM NEMPV WHERE NUMDOC='0100000637' AND ITEM=5
      SELECT * FROM NEMPVD WHERE NUMDOC='0100000637' AND ITEM=5
