SELECT A.CONSECUTIVO,MONTH(FECHA), B.RUBRO,COALESCE(B.VIGENCIA,'Actual'),SUM(B.VALOR),SUM(COALESCE(B.VLR_NOTAS,0))
FROM PTRECO A INNER JOIN PTRECOD B ON A.CONSECUTIVO=B.CONSECUTIVO AND A.RECO=B.RECO
WHERE A.CONSECUTIVO='2024'
AND A.ESTADO='Confirmado'
AND MONTH(FECHA)=7
GROUP BY A.CONSECUTIVO,MONTH(FECHA),  B.RUBRO,COALESCE(B.VIGENCIA,'Actual')


