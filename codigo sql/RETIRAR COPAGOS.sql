USE KUNIPSSAM
DECLARE @CONSECUTIVO VARCHAR(20)='010000006932'
DECLARE @CNSFACJ  VARCHAR(20)

IF EXISTS(SELECT  * FROM CIT WHERE CONSECUTIVO=@CONSECUTIVO AND VALORCOPAGO>0)
BEGIN
   UPDATE CIT SET VALORCOPAGO=0 WHERE CONSECUTIVO=@CONSECUTIVO
END
IF EXISTS(SELECT * FROM TFCJ WHERE NOADMISION=@CONSECUTIVO)
BEGIN
   SELECT @CNSFACJ=CNSFACJ FROM TFCJ WHERE NOADMISION=@CONSECUTIVO AND PROCEDENCIA='CITAS'

   DELETE TFCJDD WHERE CNSFACJ=@CNSFACJ
   DELETE TFCJD WHERE CNSFACJ=@CNSFACJ
   DELETE TFCJ WHERE CNSFACJ=@CNSFACJ
END

