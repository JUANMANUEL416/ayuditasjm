	         INNER JOIN LORD C ON A.NORDEN=C.NORDEN
	         INNER JOIN SER  D ON A.IDSERVICIO=D.IDSERVICIO
	         INNER JOIN LING E ON C.NOINGRESO=E.NOINGRESO
WHERE A.TIPOCAMPO='Alfanumerico' AND C.ESTADO='R' AND E.AMBITOTR='LX' AND E.NOADMISION='$noadmision' AND C.IDSERVICIO='$idservicio'
UNION ALL 
