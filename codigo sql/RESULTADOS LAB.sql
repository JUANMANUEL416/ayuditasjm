SELECT E.NOADMISION,A.NORDEN,A.IDSERVICIO,D.DESCSERVICIO,A.NOITEM,A.IDDATO,B.NOMBRE,ALFANUMERICO,B.UNIMED,DBO.FNK_FECHA(C.FECHARES)FECHARES
FROM LORDD A INNER JOIN LSER B ON A.IDDATO=B.IDDATO AND A.NOITEM=B.NOITEM AND A.IDSERVICIO=B.IDSERVICIO
			 INNER JOIN LORD C ON A.NORDEN=C.NORDEN
			 INNER JOIN SER  D ON A.IDSERVICIO=D.IDSERVICIO
			 INNER JOIN LING E ON C.NOINGRESO=E.NOINGRESO
WHERE A.TIPOCAMPO='LISTA' AND C.ESTADO='R' AND E.AMBITOTR='LX' AND E.NOADMISION='$noadmision' AND C.IDSERVICIO='$idservicio'
UNION ALL 
SELECT E.NOADMISION,A.NORDEN,A.IDSERVICIO,D.DESCSERVICIO,A.NOITEM,A.IDDATO,B.NOMBRE,ALFANUMERICO,B.UNIMED,DBO.FNK_FECHA(C.FECHARES)FECHARES
